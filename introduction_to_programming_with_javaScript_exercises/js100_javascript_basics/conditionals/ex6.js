let weather = 'autumn';

switch (weather) {
  case 'sunny':
    console.log(`It's a beautiful day!`);
    break;
  case 'rainy':
    console.log(`Grab your umbrella.`);
    break;
  case 'winter':
    console.log(`Wear a jacket!`);
    break;
  case 'autumn':
    console.log(`Get a sweatshirt.`);
    break;
  default:
    console.log(`Let's stay inside.`);
}